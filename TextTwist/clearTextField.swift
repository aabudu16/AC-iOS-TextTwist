import UIKit
class ClearTextField{

func clearField(threeLetterWord:UITextView, fourLetterWord:UITextView, fiveLetterWord:UITextView, sixLetterWord:UITextView){
    threeLetterWord.text = ""
    fourLetterWord.text = ""
    fiveLetterWord.text = ""
    sixLetterWord.text = ""
}
}
